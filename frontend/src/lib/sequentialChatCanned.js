/**
 * Мгновенные готовые ответы для ИИ-чата последовательного маршрута (без запроса к серверу).
 * Возвращает { text } или null — тогда вызывается API.
 */
function norm(s) {
  return String(s || '')
    .toLowerCase()
    .replace(/\s+/g, ' ')
    .trim()
}

function stopSummary(places) {
  const n = places?.length ?? 0
  if (n === 0) return { n: 0, preview: '' }
  const preview = places
    .slice(0, 5)
    .map((p) => p?.name)
    .filter(Boolean)
    .join('», «')
  return { n, preview }
}

/**
 * @param {string} message
 * @param {Array<{ id?: string, name?: string }>} places
 * @returns {{ text: string } | null}
 */
export function getCannedSequentialReply(message, places) {
  const t = norm(message)
  if (!t) return null

  const { n, preview } = stopSummary(places)

  // Точное совпадение с кнопками «Быстрый вопрос»
  if (t === 'что логично добавить дальше?') {
    if (n === 0) {
      return {
        text: 'Пока в маршруте нет остановок. Откройте список «Добавить» на странице маршрута и выберите первую винодельню — после этого можно спланировать, что взять следующим (часто логично держаться одного района: Анапа, Новороссийск, Тамань или предгорья Геленджика).',
      }
    }
    return {
      text: `Сейчас у вас ${n} остановок${preview ? `: «${preview}»` : ''}. Обычно добавляют ещё одну винодельню в том же кластере или точку питания рядом. Откройте «Добавить» — там весь каталог; ориентируйтесь на расстояние на карте и время в пути.`,
    }
  }

  if (t === 'как не перегрузить день?') {
    return {
      text: 'Лучше 2–3 винодельни в день с запасом по времени: на дегустацию и дорогу часто уходит 2–3 часа на точку. Закладывайте обед, не планируйте «всё подряд» в один день. Если устали — перенесите часть на следующий день или оставьте только любимые стили вин.',
    }
  }

  if (t === 'напомни про бронирование') {
    return {
      text: 'Дегустации и экскурсии почти всегда по записи: зайдите на сайт винодельни или позвоните заранее, особенно в выходные. Наш сервис не бронирует за вас — только подсказывает точки на карте.',
    }
  }

  // Дополнительные формулировки и ключевые слова
  if (
    (t.includes('что добавить') && (t.includes('дальше') || t.includes('следующ'))) ||
    (t.includes('куда') && t.includes('после') && t.includes('винодельн'))
  ) {
    if (n === 0) {
      return {
        text: 'Сначала добавьте первую остановку из списка «Добавить». После этого смотрите на карту: удобно наращивать маршрут по соседним винодельням или к морю/к Тамани — меньше часов в дороге.',
      }
    }
    return {
      text: `С учётом текущих остановок (${n}${preview ? `: «${preview}»` : ''}) разумно добавить точку в том же районе или «питание» между дегустациями. Конкретные имена смотрите в «Добавить» — я не подменяю официальный каталог.`,
    }
  }

  if (t.includes('перегруз') || (t.includes('день') && t.includes('много'))) {
    return {
      text: 'Чтобы день не был забитым: 2 винодельни + обед часто оптимальнее, чем 4 подряд. Учитывайте дорогу по карте и то, что после дегустаций лучше не садиться за руль — планируйте трансфер или короткие переезды.',
    }
  }

  if (t.includes('брон') || t.includes('запис') || (t.includes('дегустац') && t.includes('как'))) {
    return {
      text: 'Бронируйте дегустации и туры напрямую у винодельни (сайт или телефон). В высокий сезон места заканчиваются быстро — лучше за 2–3 дня, в выходные и раньше.',
    }
  }

  if (t.includes('погод')) {
    return {
      text: 'Прогноз и подсказки по одежде показываются на странице маршрута в блоке про погоду (после появления остановок). Перед выездом гляньте актуальный прогноз по району первой точки.',
    }
  }

  if (t.includes('привет') || t.includes('здравств') || t.includes('добрый')) {
    return {
      text: 'Здравствуйте! Я подсказываю по винному маршруту: что добавить, как разложить день, про бронь и погоду. Спросите конкретно или нажмите быстрый вопрос ниже.',
    }
  }

  if (t.includes('что ты') || t.includes('что умеешь') || t.includes('помощь')) {
    return {
      text: 'Могу кратко подсказать по маршруту: не перегрузить день, напомнить про бронирование, объяснить, как пользоваться списком «Добавить». Сложные вопросы с учётом всех ваших остановок иногда уходят в расширенный ответ на сервере.',
    }
  }

  if (t.includes('трансфер') || t.includes('на такси') || (t.includes('пить') && t.includes('рул'))) {
    return {
      text: 'После дегустаций не водите в состоянии опьянения — используйте трансфер, такси или трезвого водителя. В каталоге есть сервисные точки «трансфер» как ориентир; бронируйте у перевозчиков.',
    }
  }

  return null
}
